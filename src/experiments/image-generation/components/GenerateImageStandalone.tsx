/**
 * WordPress dependencies
 */
import { useState } from '@wordpress/element';
import { __, sprintf } from '@wordpress/i18n';
import {
	Button,
	TextareaControl,
	Spinner,
	Notice,
} from '@wordpress/components';

/**
 * Internal dependencies
 */
import { runAbility } from '../../../utils/run-ability';
import { generateAltText } from '../../../utils/generate-alt-text';
import { trimText } from '../../../utils/text';
import type {
	GeneratedImageData,
	ImageImportAbilityInput,
	UploadedImage,
} from '../types';

const { aiImageGenerationData } = window as any;

type ModalState = 'idle' | 'generating' | 'preview' | 'editing' | 'success';

/**
 * Uploads an image to the media library locally to avoid relying on functions not
 * yet available in 'develop' until PR 235 merges.
 */
async function uploadImageLocal(
	{ image, prompt }: GeneratedImageData,
	options?: { onProgress?: ( msg: string ) => void; altTextEnabled?: boolean }
): Promise< UploadedImage > {
	const onProgress = options?.onProgress;

	const params: ImageImportAbilityInput = {
		data: image.data,
		mime_type: 'image/png',
		description: sprintf(
			/* translators: 1: Provider name, 2: Model name, 3: Date, 4: Prompt */
			__( 'Generated by %1$s using %2$s on %3$s. Prompt: %4$s', 'ai' ),
			image.provider_metadata.name,
			image.model_metadata.name,
			new Date().toLocaleDateString(),
			prompt
		),
		meta: [
			{
				key: 'ai_generated',
				value: '1',
			},
		],
	};

	params.alt_text = prompt;

	const isAltTextEnabled =
		options?.altTextEnabled ?? aiImageGenerationData?.altTextEnabled;
	if ( isAltTextEnabled ) {
		try {
			onProgress?.( __( 'Generating alt text…', 'ai' ) );
			params.alt_text = await generateAltText(
				undefined,
				`data:image/png;base64,${ image.data }`
			);
		} catch ( error ) {
			params.alt_text = prompt;
		}
	}

	params.title = trimText( params.alt_text || '' );

	onProgress?.( __( 'Importing image…', 'ai' ) );

	return await runAbility( 'ai/image-import', params )
		.then( ( response: any ) => {
			if (
				response &&
				typeof response === 'object' &&
				'image' in response
			) {
				return response.image as UploadedImage;
			}

			throw new Error( 'Invalid response from image import' );
		} )
		.catch( ( error ) => {
			throw new Error( error.message );
		} );
}

/**
 * Standalone component for AI image generation in the Media Library.
 */
export function GenerateImageStandalone() {
	const [ state, setState ] = useState< ModalState >( 'idle' );
	const [ prompt, setPrompt ] = useState( '' );
	const [ editPrompt, setEditPrompt ] = useState( '' );
	const [ generatedData, setGeneratedData ] =
		useState< GeneratedImageData | null >( null );
	const [ uploadedData, setUploadedData ] = useState< UploadedImage | null >(
		null
	);
	const [ progress, setProgress ] = useState( '' );
	const [ error, setError ] = useState< string | null >( null );

	async function generate(
		activePrompt: string,
		referenceImage?: string
	): Promise< void > {
		setError( null );
		setState( 'generating' );
		setProgress( __( 'Generating image…', 'ai' ) );

		try {
			const input: any = { prompt: activePrompt };
			if ( referenceImage ) {
				input.reference_image = referenceImage;
			}

			const response = ( await runAbility(
				'ai/image-generation',
				input
			) ) as GeneratedImageData;

			if ( ! response || ! response.image ) {
				throw new Error(
					__( 'Invalid response from image generation', 'ai' )
				);
			}

			setGeneratedData( { ...response, prompt: activePrompt } );
			setState( 'preview' );
		} catch ( err: any ) {
			const message: string =
				err?.message ||
				__( 'An error occurred during image generation.', 'ai' );

			setError( message );
			setState( referenceImage ? 'editing' : 'idle' );
		}
	}

	async function handleSaveImage(): Promise< void > {
		if ( ! generatedData ) {
			return;
		}

		setError( null );
		setState( 'generating' );
		setProgress( __( 'Uploading image to Media Library…', 'ai' ) );

		try {
			const uploaded: UploadedImage = await uploadImageLocal(
				generatedData,
				{
					onProgress: setProgress,
					altTextEnabled: aiImageGenerationData?.altTextEnabled,
				}
			);

			setUploadedData( uploaded );
			setState( 'success' );
		} catch ( err: any ) {
			setError( err?.message || __( 'Failed to upload image.', 'ai' ) );
			setState( 'preview' );
		}
	}

	const previewSrc = generatedData?.image?.data
		? `data:image/png;base64,${ generatedData.image.data }`
		: null;

	return (
		<div className="ai-generate-image-standalone">
			{ state === 'success' && uploadedData && (
				<div className="ai-generate-image-standalone__success">
					<Notice status="success" isDismissible={ false }>
						{ __(
							'Image successfully added to the Media Library.',
							'ai'
						) }
					</Notice>
					<img
						src={ uploadedData.url }
						alt={ uploadedData.title }
						className="ai-generate-image-standalone__preview-image"
						style={ {
							maxWidth: '400px',
							display: 'block',
							margin: '20px 0',
						} }
					/>
					<Button
						variant="secondary"
						onClick={ () => {
							setGeneratedData( null );
							setUploadedData( null );
							setPrompt( '' );
							setState( 'idle' );
							setError( null );
						} }
					>
						{ __( 'Generate Another Image', 'ai' ) }
					</Button>
				</div>
			) }

			{ state === 'idle' && (
				<div className="ai-generate-image-standalone__idle">
					<p
						className="description"
						style={ { marginBottom: '10px' } }
					>
						{ __(
							'Describe the image you want to generate. It will be saved directly to your Media Library.',
							'ai'
						) }
					</p>
					<TextareaControl
						label={ __( 'Prompt', 'ai' ) }
						value={ prompt }
						onChange={ setPrompt }
						rows={ 4 }
						hideLabelFromVision
						__nextHasNoMarginBottom
					/>
					<div
						className="ai-generate-image-standalone__actions"
						style={ { marginTop: '15px' } }
					>
						<Button
							variant="primary"
							disabled={ ! prompt.trim() }
							onClick={ () => generate( prompt.trim() ) }
						>
							{ __( 'Generate', 'ai' ) }
						</Button>
					</div>
					{ error && (
						<div style={ { marginTop: '15px' } }>
							<Notice
								status="error"
								isDismissible={ false }
							>
								{ error }
							</Notice>
						</div>
					) }
				</div>
			) }

			{ state === 'generating' && (
				<div className="ai-generate-image-standalone__generating">
					{ previewSrc && (
						<img
							src={ previewSrc }
							alt={ generatedData?.prompt ?? '' }
							className="ai-generate-image-standalone__preview-image"
							style={ {
								maxWidth: '400px',
								opacity: 0.5,
								display: 'block',
								margin: '20px 0',
							} }
						/>
					) }
					<div
						className="ai-generate-image-standalone__spinner-row"
						style={ {
							display: 'flex',
							alignItems: 'center',
							gap: '10px',
						} }
					>
						<Spinner />
						<span>{ progress }</span>
					</div>
					{ error && (
						<div style={ { marginTop: '15px' } }>
							<Notice
								status="error"
								isDismissible={ false }
							>
								{ error }
							</Notice>
						</div>
					) }
				</div>
			) }

			{ state === 'preview' && previewSrc && (
				<div className="ai-generate-image-standalone__preview">
					<img
						src={ previewSrc }
						alt={ generatedData?.prompt ?? '' }
						className="ai-generate-image-standalone__preview-image"
						style={ {
							maxWidth: '600px',
							display: 'block',
							margin: '20px 0',
							border: '1px solid #ddd',
						} }
					/>
					<div
						className="ai-generate-image-standalone__actions"
						style={ {
							display: 'flex',
							gap: '10px',
							marginTop: '15px',
						} }
					>
						<Button variant="primary" onClick={ handleSaveImage }>
							{ __( 'Save to Media Library', 'ai' ) }
						</Button>
						<Button
							variant="secondary"
							onClick={ () => {
								setEditPrompt( '' );
								setState( 'editing' );
							} }
						>
							{ __( 'Refine Image', 'ai' ) }
						</Button>
						<Button
							variant="secondary"
							onClick={ () => generate( prompt.trim() ) }
						>
							{ __( 'Regenerate', 'ai' ) }
						</Button>
						<Button
							variant="tertiary"
							onClick={ () => {
								setGeneratedData( null );
								setState( 'idle' );
								setError( null );
							} }
						>
							{ __( 'Cancel', 'ai' ) }
						</Button>
					</div>
					{ error && (
						<div style={ { marginTop: '15px' } }>
							<Notice
								status="error"
								isDismissible={ false }
							>
								{ error }
							</Notice>
						</div>
					) }
				</div>
			) }

			{ state === 'editing' && previewSrc && (
				<div className="ai-generate-image-standalone__editing">
					<img
						src={ previewSrc }
						alt={ generatedData?.prompt ?? '' }
						className="ai-generate-image-standalone__preview-image"
						style={ {
							maxWidth: '600px',
							display: 'block',
							margin: '20px 0',
							border: '1px solid #ddd',
						} }
					/>
					<TextareaControl
						label={ __(
							'Describe the edits you want to make to the image.',
							'ai'
						) }
						value={ editPrompt }
						onChange={ setEditPrompt }
						rows={ 3 }
						__nextHasNoMarginBottom
					/>
					<div
						className="ai-generate-image-standalone__actions"
						style={ {
							display: 'flex',
							gap: '10px',
							marginTop: '15px',
						} }
					>
						<Button
							variant="primary"
							disabled={ ! editPrompt.trim() }
							onClick={ () =>
								generate(
									editPrompt.trim(),
									generatedData?.image?.data
								)
							}
						>
							{ __( 'Refine', 'ai' ) }
						</Button>
						<Button
							variant="tertiary"
							onClick={ () => {
								setState( 'preview' );
								setError( null );
							} }
						>
							{ __( 'Cancel Refinement', 'ai' ) }
						</Button>
					</div>
					{ error && (
						<div style={ { marginTop: '15px' } }>
							<Notice
								status="error"
								isDismissible={ false }
							>
								{ error }
							</Notice>
						</div>
					) }
				</div>
			) }
		</div>
	);
}
